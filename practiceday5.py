# Day5 practice git branching 
# using git branch (branch name) created a branch
# using git checkout (branch name) switched to branch 
# Now this would be uploaded on new branch and would be merged in main branch later 

class Car:
  color="Blue"
  Brand="Mercedes"
car1=Car()
print(car1.color) 
print(car1.Brand)   

# after adding , commiting , pushing to new branch 
# switching to main branch and using (git merge (branch name)) and further pushing in main 
# we get the the file pushed in branch is in main branch as new branch is merged in main branch. Thus this is the way branching works.   